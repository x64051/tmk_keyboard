void fade_led_init(void)
{

    pwm_phase = FADE_VALUES_LEN - 1;
    fade_led_key_pressed   = false;
    
    cli();
    
    // init pwm on port PB5
    DDRB = 1 << PB5;
    TCCR1A = 1 << COM1A1 | 1 << WGM00 | 1 << WGM02;
    TCCR1B = 1 << WGM02 | 1 << CS00;
    OCR1A = VALUE_PHASE_0();

    // init timer 3
  
    TCCR3A = 0; // normal, kein pwm
    TCCR3B =  1 << CS30; // keinen prescaler
    TIMSK3 = 1 << TOIE3; // interrupt bei overflow
    TCNT3 = 0; 
   
    sei();
}

ISR(TIMER3_OVF_vect)
{
    if(fade_led_key_pressed){
        OCR1A = VALUE_PHASE_0();
        pwm_phase = FADE_VALUES_LEN - 1;
    } else if(pwm_phase >= 0){
        /* OCR1A = pgm_read_word(fade_values + FADE_VALUES_LEN - pwm_phase); */
        OCR1A = pgm_read_word(fade_values + pwm_phase);
        pwm_phase--;
    }
}



